export const getTemplatesByIndustry = (industryId: string): Template[] => {
  return TEMPLATES.filter(t => t.industryId === industryId);
};

export const getTemplatesByCategory = (category: string): Template[] => {
  return TEMPLATES.filter(t => t.category === category);
};

export const getTrendingTemplates = (): Template[] => {
  return TEMPLATES.filter(t => t.isTrending).sort((a, b) => b.usageCount - a.usageCount);
};

export const getNewTemplates = (): Template[] => {
  return TEMPLATES.filter(t => t.isNew);
};

export const getPremiumTemplates = (): Template[] => {
  return TEMPLATES.filter(t => t.isPremium);
};
